//Java是物件導向程式，所有東西都是物件
//hello是個物件
public class hello {   //定義物件(死背)
    public static void main(String[] args){   //定義起始點(死背)
        System.out.println("hello");   //系統取用Java裡out這個物件的println方法  || out : 出來方法的物件  || println : 印出  || "hello" : 是字串(String)
        //合併輸出 Java和Python不一樣 Java可以字串和數字一起輸出，Python則必須將 數字轉為字串(str) 輸出
    }
}
